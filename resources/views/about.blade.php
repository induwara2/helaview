Founded in 1996 in Amsterdam, helaview.com has grown from a small Dutch startup to one of the world’s leading digital travel companies. Part of helaview Holdings Inc. (NASDAQ: BKNG), helaview.com’s mission is to make it easier for everyone to experience the world.

By investing in the technology that helps take the friction out of travel, helaview.com seamlessly connects millions of travellers with memorable experiences, a range of transport options and incredible places to stay - from homes to hotels and much more. As one of the world’s largest travel marketplaces for both established brands and entrepreneurs of all sizes, helaview.com enables properties all over the world to reach a global audience and grow their businesses.

helaview.com is available in 43 languages and offers more than 28 million total reported accommodation listings, including over 6.2 million listings alone of homes, apartments and other unique places to stay. No matter where you want to go or what you want to do, helaview.com makes it easy and backs it all up with 24/7 customer support.
